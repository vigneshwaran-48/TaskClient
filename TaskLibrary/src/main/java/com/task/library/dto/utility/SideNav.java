package com.task.library.dto.utility;

public class SideNav {

    private String id;
    private String name;
    private int count;
    private String iconClassNames;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getIconClassNames() {
        return iconClassNames;
    }

    public void setIconClassNames(String iconClassNames) {
        this.iconClassNames = iconClassNames;
    }
}
